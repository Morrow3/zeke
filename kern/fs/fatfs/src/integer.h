/*-------------------------------------------*/
/* Integer type definitions for FatFs module */
/*-------------------------------------------*/

#ifndef _FF_INTEGER
#define _FF_INTEGER

#include <stdint.h>

/* These types MUST be 16 bit */
typedef uint16_t        WORD;
typedef uint16_t        WCHAR;

/* These types MUST be 32 bit */
typedef uint32_t        DWORD;

#endif
