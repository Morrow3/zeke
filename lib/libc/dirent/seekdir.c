#include <unistd.h>
#include <dirent.h>

void seekdir(DIR * dirp, off_t loc)
{
    lseek(dirp->dd_fd, loc, SEEK_SET);
    dirp->dd_count = 0;
    dirp->dd_loc = 0;
}
